//! Internationalisation — UI string translations.
//!
//! Every piece of user-visible text in the IDE and Form Designer interface is
//! stored here.  COBOL data-item names, generated source code, and
//! programmatic diagnostic messages are intentionally **not** translated.
//!
//! # Supported languages
//! - English  🇺🇸
//! - Español  🇪🇸
//! - Português 🇧🇷
//! - 日本語   🇯🇵
//! - 中文     🇨🇳

// ── Language ──────────────────────────────────────────────────────────────────

/// Selected UI language.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum Language {
    #[default]
    English,
    Spanish,
    Portuguese,
    Japanese,
    Chinese,
    French,
}

impl Language {
    /// All variants in display order.
    pub const ALL: &'static [Language] = &[
        Language::English,
        Language::Portuguese,
        Language::Spanish,
        Language::French,
        Language::Japanese,
        Language::Chinese,
    ];

    /// Name shown in the language selector — written in the language itself.
    pub fn native_name(self) -> &'static str {
        match self {
            Language::English    => "🇺🇸 English",
            Language::Spanish    => "🇪🇸 Español",
            Language::Portuguese => "🇧🇷 Português",
            Language::Japanese   => "🇯🇵 日本語",
            Language::Chinese    => "🇨🇳 中文",
            Language::French     => "🇫🇷 Français",
        }
    }

    /// Return the full translation table for this language.
    pub fn tr(self) -> Tr {
        match self {
            Language::English    => EN,
            Language::Spanish    => ES,
            Language::Portuguese => PT,
            Language::Japanese   => JA,
            Language::Chinese    => ZH,
            Language::French     => FR,
        }
    }
}

