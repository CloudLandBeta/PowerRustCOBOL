// ── Helper for toolbox ────────────────────────────────────────────────────────

impl Tr {
    /// Look up a category's translated display name by its internal key string.
    pub fn category_name<'a>(&self, key: &'a str) -> &'a str {
        match key {
            "Common"    => self.cat_common,
            "Container" => self.cat_container,
            "Data"      => self.cat_data,
            "Graphics"  => self.cat_graphics,
            "Menu"      => self.cat_menu,
            "NonVisual" => self.cat_nonvisual,
            "Charts"    => self.cat_charts,
            "Dialogs"   => self.cat_dialogs,
            other       => other,
        }
    }
}

