// ── i18n behavioral tests ──────────────────────────────────────────────────────
// Verifies the IDE-UI translation layer (form/COBOL content stays English by the
// project's CRITICAL constraint, so it is intentionally NOT translated).
#[cfg(test)]
mod i18n_tests {
    use super::*;

    /// A representative spread of common UI strings (across menus, panels, labels).
    fn sample(t: &Tr) -> Vec<&'static str> {
        vec![
            t.menu_file, t.menu_run, t.menu_run_btn,
            t.close_save, t.close_cancel,
            t.panel_output, t.panel_clear, t.panel_debugger,
            t.panel_project, t.panel_sources, t.panel_forms, t.panel_assets,
            t.lbl_back_color, t.lbl_fore_color, t.lbl_tab_order,
        ]
    }

    #[test]
    fn all_languages_with_unique_native_names() {
        assert_eq!(Language::ALL.len(), 6, "expected 6 languages (EN/ES/PT/JA/ZH/FR)");
        let names: Vec<&str> = Language::ALL.iter().map(|l| l.native_name()).collect();
        for n in &names {
            assert!(!n.trim().is_empty(), "empty native name");
        }
        let mut uniq = names.clone();
        uniq.sort();
        uniq.dedup();
        assert_eq!(uniq.len(), names.len(), "native names are not unique");
    }

    #[test]
    fn no_empty_ui_translations() {
        for &lang in Language::ALL {
            let t = lang.tr();
            for (i, s) in sample(&t).iter().enumerate() {
                assert!(!s.trim().is_empty(), "{lang:?}: sample UI field #{i} is empty");
            }
        }
    }

    #[test]
    fn non_english_is_actually_translated() {
        let en = sample(&Language::English.tr());
        for &lang in Language::ALL {
            if lang == Language::English {
                continue;
            }
            let other = sample(&lang.tr());
            let diff = en.iter().zip(other.iter()).filter(|(a, b)| a != b).count();
            assert!(
                diff * 100 / en.len() >= 60,
                "{lang:?}: only {diff}/{} sampled UI strings differ from English — looks untranslated",
                en.len()
            );
        }
    }
}
